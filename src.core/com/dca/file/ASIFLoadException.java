package com.dca.file;

public class ASIFLoadException extends Exception{


	public ASIFLoadException() {
	}

	public ASIFLoadException(String message) {
		super(message);
	}

	public ASIFLoadException(Throwable cause) {
		super(cause);
	}

	public ASIFLoadException(String message, Throwable cause) {
		super(message, cause);
	}

}
